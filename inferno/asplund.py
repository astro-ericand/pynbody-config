    def relative(self, X, Y, X_str: str, Y_str: str):
        """ Caluculate the the abundance [X/Y] relative solar values from
            mass fraction X,Y. Strings used to indicate what element.
        """
        import numpy as np
        np.seterr(all='ignore')

        return np.log10((X/self.atomic_mass[X_str]) / (Y/self.atomic_mass[Y_str])) \
            - (self.solar[X_str] - self.solar[Y_str])
